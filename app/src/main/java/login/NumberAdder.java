public class NumberAdder {

    public static int addNumbers(int one, int two){
        int x = one;
        int y = two;
        int z = x + y;
        return z;
    }

    public static int substractNumbers(int one, int two){
        int x = one;
        int y = two;
        int z = x + y;
        return z;
    }


}
